export type Translation = typeof zh;

export function resolveLanguage(preference: AppLanguage): "zh" | "en" {
  if (preference === "zh" || preference === "en") return preference;
  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

export function getTranslation(preference: AppLanguage): Translation {
  return resolveLanguage(preference) === "zh" ? zh : en;
}
